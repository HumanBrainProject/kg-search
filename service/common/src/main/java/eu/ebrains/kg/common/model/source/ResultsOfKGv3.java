package eu.ebrains.kg.common.model.source;

import eu.ebrains.kg.common.model.ErrorReport;

import java.util.List;

public class ResultsOfKGv3<E> implements ResultsOfKG<E>{

    public static class Error{
        private Integer code;
        private String message;

        public Integer getCode() {
            return code;
        }

        public void setCode(Integer code) {
            this.code = code;
        }

        public String getMessage() {
            return message;
        }

        public void setMessage(String message) {
            this.message = message;
        }
    }

    private List<E> data;
    private String message;
    private Error error;
    private Long startTime;
    private Long durationInMs;
    private Integer total;
    private Integer size;
    private Integer from;
    private ErrorReport errors;

    @Override
    public ErrorReport getErrors() {
        return errors;
    }

    @Override
    public void setErrors(ErrorReport errors) {
        this.errors = errors;
    }

    public List<E> getData() {
        return data;
    }

    public void setData(List<E> data) {
        this.data = data;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Error getError() {
        return error;
    }

    public void setError(Error error) {
        this.error = error;
    }

    public Long getStartTime() {
        return startTime;
    }

    public void setStartTime(Long startTime) {
        this.startTime = startTime;
    }

    public Long getDurationInMs() {
        return durationInMs;
    }

    public void setDurationInMs(Long durationInMs) {
        this.durationInMs = durationInMs;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public Integer getFrom() {
        return from;
    }

    public void setFrom(Integer from) {
        this.from = from;
    }
}
